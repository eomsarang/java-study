package variable;

public class Test01 {
  public static void main(String[] args) {
    // 변수 선언
    byte b;

    // 변수 값 저장
    b = 100;

    // 변수 선언 + 값 저장
    // '=' : assignment operator(할당 연산자, 배정 연산자, 대입 연산자)
    byte b2 = 100;

    // 여러 변수 선언
    byte b3, b4, b5;

    // 여러 변수 선언 + 값 할당
    byte b6 = 100, b7, b8 = 50;
  }
}
